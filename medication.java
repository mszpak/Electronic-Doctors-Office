package proj1;
public class medication
{

    private String name;
    private String ID;
    
    public medication(String ID, String name)
    {
        this.name = name;
        this.ID = ID;
    }
    
    public String getName()
    {
        return name;
    }
    
    public String getID()
    {
        return ID;
    }
    
	
	
}